/*참새를 정의한다. */
package animal;
public class Sparrow extends Bird{

	String name="참새";

	public void jjack(){
		System.out.println("쮁쮁주꼑주꼐ㅉ꾸");
	}

	//자식 클래스에서 부모와 100% 동일한 메서드를 정의하는 기법을 오버라이딩이라고 한다..(Overriding) 
	//왜 지원하는가..?
	public void fly(){//부모의 메서드를 완전히 재정의한것임....over riding이 확인되면 우선순위를 자식에게줌(자식의 업그레이드한것을 존중해줌)
		/*
		오버로딩vs오버라이딩

		오버로딩은 같은 클래스내에서 기능이 비슷한 메서드명을 굳이 새로만들지말고,
		매개변수 갯수와 자료형으로 구분하면 중복정의를 인정해주겠다. 

		오버라이딩은 상속관계에서 자식이 부모의 메서드를 재정의 하는 기법..
		*/
		System.out.println("참새가 날아요");
	}
	public static void main(String []args){
		Sparrow sp = new Sparrow();
		sp.fly(); //참새의 메서드 호출 
		Bird bird = new Bird();
		bird.fly();//부모인 새의 매서드 호출 
		
		Bird bird2=new Sparrow();
		bird2.fly(); 
		sp.jjack();

	}
}
